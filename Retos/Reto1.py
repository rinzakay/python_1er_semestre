# Solicitar el nombre y la edad del usuario desde la consola
nombre = input("Por favor, ingresa tu nombre: ")
edad = int(input("Por favor, ingresa tu edad: "))

# Calcular la edad en 20 años
total_edad = edad + 20

# Mostrar el mensaje con el formato solicitado
print(f"Hola mi nombre es {nombre}, tengo {edad} años")
print(f"Y mi edad en 20 años será {total_edad} años")